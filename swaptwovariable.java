package com.java;

public class swaptwovariable {
	 public static void main(String args[])
	 {
		 int x=100;
	     int y=80;
		 int temp=0;
		 System.out.println("before swap");
		 System.out.println("x="+x);
		 System.out.println("y="+y);
		 temp=x;
		 x=y;
		 y=temp;
		 System.out.println("after swap");
		 System.out.println("x="+x);
		 System.out.println("y="+y);

	 }
}
